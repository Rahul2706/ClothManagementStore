    private void btnAssignActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAssignActionPerformed
        String message = "";
        boolean allvalid = true;
        if (jcbBrandId.getSelectedIndex() == 0) {
            message += "Please Chosse Any Category\n\n ";
            allvalid = false;
        }
        if (jcbSubCategoryId.getSelectedIndex() == 0) {
            message += "Please Chosse Any Category\n\n ";
            allvalid = false;
        }

        String PriceName = jtfPrice.getText().trim();
        if (Validations.isEmpty(PriceName)) {
            message += "Please Enter Some Value In Price Box\n\n";
            lblPriceNameError.setVisible(true);
            lblPriceNameError.setToolTipText("Please Enter Some Value In PriceName Box");
            allvalid = false;
        }

        

        String DiscountName = jtfDiscount.getText().trim();
        if (Validations.isEmpty(DiscountName)) {
            message += "Please Enter Some Value In Discount Box\n\n";
            lblDiscountNameError.setVisible(true);
            lblDiscountNameError.setToolTipText("Please Enter Some Value In DiscountName Box");
            allvalid = false;
        }
        String DescriptionName = jtfDescription.getText().trim();
        if (Validations.isEmpty(DescriptionName)) {
            message += "Please Enter Some Value In Description Box\n\n";
            lblDescriptionNameError.setVisible(true);
            lblDescriptionNameError.setToolTipText("Please Enter Some Value In Description Name Box");
            allvalid = false;
        }
        if (allvalid) {
            ClothInfoTO record = new ClothInfoTO();
            SubCategoryTO sct = (SubCategoryTO) jcbSubCategoryId.getSelectedItem();
            record.setSubcategoryid(sct.getSubcategoryid());
            BrandInfoTO bit = (BrandInfoTO) jcbBrandId.getSelectedItem();
            record.setBrandid(bit.getBrandid());
            record.setPrice(Float.valueOf(PriceName));
            record.setDiscount(Float.valueOf(DiscountName));
            record.setdescription(DescriptionName);
            ClothInfoDAO action = new ClothInfoDAO();
            if (btnAssign.getText().contains("Assign")) {
                if (action.insertRecord(record)) {
                    message = "ClothInfo is Assigned";
                } else {
                    message = "Insertion Failure" + action.getError_message();
                    allvalid = false;
                }
            } else if (btnAssign.getText().contains("Update")) {
                record.setClothid(clothid);
                if (action.updateRecord(record)) {
                    message = " ClothInfo is Updation";

                } else {
                    message = "Updation Failure Due To : " + action.getError_message();
                }
                CommonOperations.showInternalFrame(getDesktopPane(), new ViewClothInfo());
                dispose();
            } else {
                message = "Invalid Operation";
            }

        }

        if (allvalid) {
            JOptionPane.showMessageDialog(this, message);
        } else {
            JOptionPane.showMessageDialog(this, message, "Some Failure", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnAssignActionPerformed


