    public AddClothInfo() {
        initComponents();
        jcbBrandId.removeAllItems();
        jcbBrandId.addItem("Select Any Brand");
        List<BrandInfoTO> brandId = new BrandInfoDAO().getAllRecord();
        if (brandId != null && brandId.size() > 0) {
            for (BrandInfoTO bid : brandId) {
                jcbBrandId.addItem(bid);
            }
        }
        jcbSubCategoryId.removeAllItems();
        jcbSubCategoryId.addItem("Select Any SubCategory");
        List<SubCategoryTO> subcategory = new SubCategoryDAO().getAllRecord();
        if (subcategory != null && subcategory.size() > 0) {
            for (SubCategoryTO sct : subcategory) {
                jcbSubCategoryId.addItem(sct);
            }
        }
        lblPriceNameError.setVisible(false);
        lblDiscountNameError.setVisible(false);
        lblDescriptionNameError.setVisible(false);

    }

    public AddClothInfo(int clothid) {
        this();
        ClothInfoTO record = new ClothInfoDAO().getRecord(clothid);
        if (record != null) {
            this.clothid = clothid;
            setTitle("Update ClothInfo");
            btnAssign.setText("Update SubCategory Details");

            for (int i = 1; i < jcbBrandId.getItemCount(); i++) {
                BrandInfoTO bit = (BrandInfoTO) jcbBrandId.getItemAt(i);
                if (bit.getBrandid() == (record.getBrandid())) {
                    jcbBrandId.setSelectedIndex(i);
                    break;
                }
            }
            for (int i = 1; i < jcbSubCategoryId.getItemCount(); i++) {
                SubCategoryTO sct = (SubCategoryTO) jcbSubCategoryId.getItemAt(i);
                if (sct.getSubcategoryid() == (record.getSubcategoryid())) {
                    jcbSubCategoryId.setSelectedIndex(i);
                    break;
                }
            }
            jtfPrice.setText(String.valueOf(record.getPrice()));
            jtfDiscount.setText(String.valueOf(record.getDiscount()));
            jtfDescription.setText(record.getdescription());
        }

    }

